class Solution(object):
    def lengthOfLongestSubstring(self, s):
        """
        :type s: str
        :rtype: int
        """
        return 1

MySolution = Solution()

s = "abcabcbb"

print(MySolution.lengthOfLongestSubstring(s))# 3
# #########################################

s = "bbbbb"

print(MySolution.lengthOfLongestSubstring(s))# 1
# #########################################

s = "pwwkew"

print(MySolution.lengthOfLongestSubstring(s))# 3
